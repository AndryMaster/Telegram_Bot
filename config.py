def format_text(text):
    res = []
    for string in text.split('\n'):
        s = ''
        for i in range(len(string)):
            if string[i].isdigit():
                s = '\t' + string[i:]
                break
            if string[i].isalpha():
                s = string[i:]
                break
        res.append(s)
    return '\n'.join(res)


